function get_app_env($name, $default = null)
{
    $result = getenv(ENV_PREFIX . strtoupper(str_replace('.', '_', $name)));

    if (false !== $result) {
        if ('false' === $result) {
            $result = false;
        } elseif ('true' === $result) {
            $result = true;
        }

        return $result;
    }

    return $default;
}

